#include "Fixed.h"
#include <iostream>

int main()
{
	Fixed a;
	Fixed const b( Fixed( -300.f ) * Fixed( 300 ) );
	std::cout << a << std::endl;
	std::cout << ++a << std::endl;
	std::cout << a << std::endl;
	std::cout << a++ << std::endl;
	std::cout << a << std::endl;
	std::cout << b << std::endl;
	std::cout << Fixed::max( a, b ) << std::endl;
	return 0;
}